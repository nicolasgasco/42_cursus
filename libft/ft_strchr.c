char	*ft_strchr(const char *s, int c)
{
	while (*s != c && *s != '\0')
		s++;
	if (*s == '\0')
		if (c != '\0')
			return (0);
	if (*(s + 1) == c)
		s++;
	return ((char *)s);
}
