#include <stddef.h>

void ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int		i;

	i = 0;
	if (!s || !f)
		return (NULL);
	while (s[i] != '\0')
	{
		f(i, &s[i]);
		i++;
	}
}
